    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked

        try {
            double varianza;
            double nivelc;
            double tamaño;

            if ((varianzaM.getText().length() == 0) || (n.getText().length() == 0)) {
                //emite señal de error
                getToolkit().beep();
                JOptionPane.showMessageDialog(this, "Ingrese el valor de la proporcion muestral y el tamaño de la muestra para poder hallar el intervalo de confianza", "Error de Campo vacio", JOptionPane.ERROR_MESSAGE);

            } else if ((Double.parseDouble(n.getText()) < 1)) {
                //emite señal de error
                getToolkit().beep();
                JOptionPane.showMessageDialog(this, "No se pueden tener una muetra de tamaño 0 o menos", "tamaño de la muestra no valido", JOptionPane.ERROR_MESSAGE);

            } else {

                //DecimalFormat df = new DecimalFormat("0.00000");
                varianza = Double.parseDouble(varianzaM.getText());
                tamaño = Double.parseDouble(n.getText());

                if ((nc.getText().length() == 0)) {
                    JOptionPane.showMessageDialog(this, "se tomara 95% como nivel de confianza", "Nivel de confianza", HEIGHT);
                    nivelc = 95;
                } else {
                    nivelc = Double.parseDouble(nc.getText());
                }

                if ((nivelc > 100) || (nivelc < 0)) {
                    //emite señal de error
                    getToolkit().beep();
                    JOptionPane.showMessageDialog(this, "No es valido tener un nivel de confianza mayor a 100% o menor al 0%", "Nivel de confianza no valido", JOptionPane.ERROR_MESSAGE);

                } else {

                    double porcentaje;
                    porcentaje = nivelc / 100;

                    double alfa2 = (1 - porcentaje) / 2;

                    //////////////////
                    double sup;
                    double inf;
                    double gl = tamaño - 1;

                    ChiSquaredDistribution chi = new ChiSquaredDistribution(gl);
                    chi.inverseCumulativeProbability(alfa2);

                    sup = ((tamaño - 1) * varianza) / (chi.inverseCumulativeProbability(1 - alfa2));
                    inf = ((tamaño - 1) * varianza) / (chi.inverseCumulativeProbability(alfa2));

                    superior.setText(String.valueOf(inf));
                    inferior.setText(String.valueOf(sup));

                    dessup.setText(String.valueOf(Math.sqrt(inf)));
                    desinf.setText(String.valueOf(Math.sqrt(sup)));

                }

            }
        } catch (Exception e) {

            JOptionPane.showMessageDialog(this, "Hay un dato invalido", "Error con algun dato mal ingresado", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_jButton2MouseClicked

