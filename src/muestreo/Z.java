    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked

        try {
            DecimalFormat df = new DecimalFormat("0.00000");
            DecimalFormat dfx = new DecimalFormat("0.00");
            if ((z.getText().length() == 0) && (zcritica.getText().length() == 0)) {
                //emite se単al de error
                getToolkit().beep();
                JOptionPane.showMessageDialog(this, "Ingrese un valor para encontrar", "Error de Campo vacio", JOptionPane.ERROR_MESSAGE);

            } else if ((z.getText().length() != 0) && (zcritica.getText().length() != 0)) {

                //emite se単al de error
                getToolkit().beep();
                JOptionPane.showMessageDialog(this, "Solo puedes buscar uno de los datos a la vez", "Error de busqueda", JOptionPane.ERROR_MESSAGE);
            } else {

                if (z.getText().length() == 0) {

                    double zcritic = Double.parseDouble(zcritica.getText());
                    NormalDistribution distribution = new NormalDistribution();
                    double outcomeRisk = distribution.inverseCumulativeProbability(zcritic);
                    z.setText(dfx.format(outcomeRisk));
                }

                if (zcritica.getText().length() == 0) {

                    double zi = Double.parseDouble(z.getText());

                    if (zi > 4.4) {

                        zcritica.setText(String.valueOf(1.00000));

                    } else if (zi < -4.4) {

                        zcritica.setText(String.valueOf(0.00000));

                    } else {

                        zcritica.setText(df.format(calculazacum(zi)));

                    }
                }

            }
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(this, "Hay un dato invalido", "Error con algun dato mal ingresado", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_jButton1MouseClicked

